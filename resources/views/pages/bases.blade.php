                Semana 1: <span class="underline">“LA SONRISA DE MI CASA” </span> (27 de mayo al 2 de junio 2024)
                Descripción del reto: Toma 10 fotografías creativas de tu propia autoría de objetos que estén dentro de tu
                casa
                (cuartos, sala, comedor, cocina, patio o jardín) que formen claramente una cara sonriendo. <br>
                Requisitos: <br>
                -Las caras deben tener dos ojos y una boca formando una línea curva hacia arriba es decir se debe distinguir
                claramente la sonrisa. <br>
                -Los objetos pueden incluir, pero no se limitan a: un sillón, un cojín, una figura de porcelana, un enchufe
                de
                luz, una toma de agua, una lavadora, estatuas, etc. <br>
                -Las 10 fotografías deben ser de objetos diferentes y no se permiten repeticiones de objetos del mismo tipo,
                aunque se tomen desde distintos ángulos, por ejemplo: si se envían dos fotografías de apagadores, aunque
                sean
                diferentes apagadores, el tipo de objeto es el mismo; y por lo tanto solo se contará una fotografía para la
                participación <br>
                -Solo se permitirán fotografías de objetos que tengan una sonrisa claramente definida, con la boca
                flexionada
                hacia arriba (puede ser abierta o cerrada) <br>
                -No se tomarán en cuenta fotografías de caras o sonrisas construidas a partir de distintos objetos, o que se
                formen de varios elementos de objetos diferentes, es decir que no pertenezcan al mismo objeto; si los
                elementos
                no forman parte de un mismo objeto, no será considerada como una cara sonriendo, por ejemplo: una sonrisa
                construida de dos gomas y un lápiz. <br>
                -La sonrisa se podrá formar a partir de diferentes elementos siempre y cuando sean de un mismo objeto, por
                ejemplo: en un coche las calaveras y la defensa son los elementos de un mismo objeto
                <br><br>

