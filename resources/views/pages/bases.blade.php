                -El filtro de la sonrisa MARUCHAN tiene un efecto que contiene la frase “QUE ANTOJO DE REÍRME” y el efecto
                de
                una sonrisa, que podrás colocar en las caras de las personas fotografiadas.<br>
                -Las personas fotografiadas deben sostener un producto Maruchan en cada foto.<br>
                -El rostro fotografiado debe ser claro, las fotografías borrosas no serán válidas.<br>
                -Las 10 fotografías deben ser de personas diferentes y no se permiten repeticiones.
